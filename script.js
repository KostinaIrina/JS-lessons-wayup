// let
// объявление переменной
let number = 16; 
// что бы изменить значение переменной не надо опять писать let. Оно пишется 1 раз.
number = 20;
console.log(number); 
// const. используем эту переменную всегда только в том случае, когда мы точно знаем, что ее значение не будет изменяться никогда.
const number2 = 100;
console.log(number2);
